
// few conversion examples 

// string to boolean
let numberInString = "-1000"
let convertTo = Boolean(numberInString)
console.log(convertTo)


// null to boolean
let nullValue = null
let convertNull = Boolean(nullValue)
console.log(convertNull);


// undefined to boolean 
let undefinedValue = undefined
let convertUndefined = Boolean(undefinedValue)
console.log(convertUndefined);
 